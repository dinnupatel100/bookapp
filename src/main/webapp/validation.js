  function verifyPassword() {  
    var pw = document.getElementById("pass").value;  
    //check empty password field  
    if(pw == "") {  
       document.getElementById("message").innerHTML = "Fill the password please!";  
       return false;  
    }  
     
   //minimum password length validation  
    if(pw.length < 8) {  
       document.getElementById("message").innerHTML = "Atleast 8 characters";  
       return false;  
    }  
    
  //maximum length of password validation  
    if(pw.length > 15) {  
       document.getElementById("message").innerHTML = "Not exceed 15 characters";  
       return false;  
    } 
      
    var conf = document.getElementById("confirm-pass").value;
    if(pw!=conf){
        document.getElementById("message2").innerHTML = "Password not matched";
       return false;
    }
    else
    {
        alert("Registration Successful");
    }
}